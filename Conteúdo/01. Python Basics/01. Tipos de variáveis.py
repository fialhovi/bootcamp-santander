# Variavel x recebe um valor inteiro:
x = 5
print(f'x = {x}  ', type(x))
print()

# Variavel preco recebe um valor float:
preco = 6.74
print(f'Preço vale R$ {preco}', type(preco))
print()

# Variavel cidade recebe um valor string:
cidade = 'Rio de Janeiro'
print(f'Cidade: {cidade}', type(cidade))
print()

# Variavel disponivel recebe um valor boolean:
disponivel = True
print(f'Disponível: {disponivel}', type(disponivel))
